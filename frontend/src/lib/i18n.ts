const INTERPOLATION_PATTERN = /\{(\w+)\}/g;

export const translate = (
  locale: SupportedLocale,
  key: TranslationKey,
  vars?: TranslationVariables,
) => {
  const source = TRANSLATIONS[locale] ?? TRANSLATIONS.en;
  const template = source[key] || TRANSLATIONS.en[key] || key;

  if (!vars) {
    return template;
  }

  return template.replaceAll(INTERPOLATION_PATTERN, (_, variableKey: string) => {
    const value = vars[variableKey];
    return value === undefined || value === null
      ? `{${variableKey}}`
      : String(value);
  });
};

export const useI18n = () => {
  const locale = usePersonalizationStore((state) => state.locale);

  const t = useCallback(
    (key: TranslationKey, vars?: TranslationVariables) => {
      return translate(locale, key, vars);
    },
    [locale],
  );

  return {
    locale,
    t,
  };
};

export const getDateFnsLocale = (locale: SupportedLocale) => {
  return locale === "vi" ? viVN : enUS;
};

export const formatAbsoluteDateTime = (
  value: Date,
  locale: SupportedLocale,
) => {
  const normalizedLocale = locale === "vi" ? "vi-VN" : "en-US";

  return new Intl.DateTimeFormat(normalizedLocale, {
    year: "numeric",
    month: "short",
    day: "2-digit",
    hour: "2-digit",
    minute: "2-digit",
  }).format(value);
};

